# Herança simples - Relações entre classes
# Associação - usa, Agregação - tem
# Composição - É dono de, Herança - É um
#
# Herança vs Composição
#
# Classe principal (Pessoa)
#   -> super class, base class, parent class
# Classes filhas (Cliente)
#   -> sub class, child class, derived class

# O Cliente se generaliza em Pessoa
# A pessoa se especializa em Cliente
# Cliente estende Pessoa por Herança
# Cliente (Sub) --> Pessoa (Super)
# Cliente é do tipo Pessoa (herda caracteristicas de Pessoa)
# Classe - você está criando seu próprio tipo
# Faz com que uma classe muito especifica seja mais genérica
# Podendo utilizar caracteristicas dessa classe em outras classes.
